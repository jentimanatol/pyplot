'''Find the midrange for the given sample data.
 3   6   9   0   4   1   11   5   9   14   3   8   2   15   0   9
'''
# calculate the midrange
# Midrange is the average of the minimum and maximum values in a data set.
# Formula: Midrange = (minimum value + maximum value) / 2

# Sample data
data = [3, 6, 9, 0, 4, 1, 11, 5, 9, 14, 3, 8, 2, 15, 0, 9]

# Midrange
midrange = (min(data) + max(data)) / 2
print(midrange)
# print the data.lenght
print(len(data))
# print the data
print(data)
# print the minimum value
print(min(data))
# print the maximum value
print(max(data))
# print the midrange
print(midrange)

